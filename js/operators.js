
//assignment operator
let a=5;
let b=6;
//arithmetic operator
console.log("Add",a+b);
console.log("sub",a-b);
console.log("mul",a*b);
console.log("div",a/b);
console.log("mod",a%b);
//unary operator
console.log("pre increment",a);
console.log("pre increment",++a);
console.log("post increment",a++);
console.log("post increment",a);
console.log("pre decrement",a);
console.log("pre decrement",--a);
console.log("post decrement",a--);
console.log("post decrement",a);
//conditional or relational
console.log(a>b);
console.log(a<b);
console.log(a>=b);
console.log(a<=b);
console.log(a===b);
console.log(a==b);
console.log(a!=b);
console.log(a!==b);
//bitwise
console.log(a&b);
console.log(a|b);
console.log(~a);
console.log(a^b);
console.log(a>>b);
console.log(a<<b);
console.log(a>>>b);
//logical
console.log(a==b&&b==a);
console.log(a==b||b==a);
console.log(!(a==b));
//ternary operator
let num=10;
console.log((num%2==0)?"even":"odd");























